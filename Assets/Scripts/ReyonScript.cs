using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class ReyonScript : MonoBehaviour
{
    PurseCollecting purseScript;

    List<GameObject> reyonList = new List<GameObject>();
    public Transform[] reyonPos = new Transform[3];
    private void Start()
    {
        reyonList.Capacity = 3;
        purseScript = GameObject.FindGameObjectWithTag("Player").GetComponent<PurseCollecting>();
    }
    private void OnCollisionEnter(Collision collision)
    {
        if (collision.other.tag == "Player" && (reyonList.Count < reyonList.Capacity))
        {

            for (int i = 0; i < purseScript.purseList.Count; i++)
            {
                purseScript.purseList[i].transform.parent = null;
                purseScript.purseList[i].transform.position = reyonPos[i].position;
                reyonList.Add(purseScript.purseList[i]);
            }
            purseScript.purseList.Clear();

        }
    }
}
